#include "libft.h"

double		ft_pow(double n, int s)
{
	double	m;
	int		c;

	if (s == 0)
		return (1);
	c = 1;
	if (s < 0)
	{
		s *= -1;
		c = -1;
	}
	m = n;
	while (s-- != 1)
		m *= n;
	if (c == -1)
		m = 1 / m;
	return (m);
}
