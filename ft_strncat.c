#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t a;

	a = ft_strlen(s1);
	while (*s2 && n--)
		s1[a++] = *s2++;
	s1[a] = '\0';
	return (s1);
}
